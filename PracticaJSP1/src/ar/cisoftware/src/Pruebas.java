package ar.cisoftware.src;

public class Pruebas {
	//atributos
	private int resultado;
	//metodos
	public int metodoSuma (int i, int e){
		resultado = i + e;
		return resultado;
	}
	public int metodoResta (int i, int e){	
		resultado = i - e;
		return resultado;
	}
	public int metodoMulti (int i, int e){
		resultado = i * e;
		return resultado;
	}
}
